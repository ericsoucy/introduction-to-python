# Linear search
# Time Complexity: O(n)
# Space Complexity: O(1)
# iterates through a list of items, checking each item from the beginning until the target item is found.
# If the target item is found, the index where it is located in the list is returned
# If the target item is not found, -1 is returned.
# not efficient for a large list of items

# Binary search
# Time Complexity: O(log n)
# Space Complexity: O(1)
# dividing a list of items in half, and checking if the target value is in the middle of the list
# If the target value is less than the middle value, the search continues in the left half of the list
# more efficient algorithm for searching through a large list of items
# If the target item is not found, -1 is returned.

# Merge sort
# Time Complexity: O(n log n)
# Space Complexity: O(n)
# divides a list of items into smaller sublists, sorts each sublist, and then merges the sorted sublists back together to form a single sorted list.


